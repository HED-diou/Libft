#include "libft.h"

int	ft_atoi(char *c)
{
	int	i;
	int	x;
	int	out;

	out = 0;
	x = 1;
	i = 0;
	while (c[i] == ' ' || c[i] == '\t' 
		|| c[i] == '\v'  || c[i] == '\f'  
		|| c[i] == '\r')
		i++;
	if (c[i] == '-' || c[i] == '+')
	{
		if (c[i] == '-')
			x = x * -1;
		i++;
	}
	while (c[i] >= '0' && c[i] <= '9')
	{
		out = (out * 10) + (c[i] - '0');
		i++;
	}
	out = out * x;
	return (out);
}
